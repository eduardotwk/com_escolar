  <p1>Definición
Los y las estudiantes necesitan una retroalimentación informada, que es distinto al típico informe del rendimiento que reciben en el colegio. Esta retroalimentación debe ayudarles a reflexionar sobre sus aspectos positivos, dificultades y desafíos.  Se trata de entregar retroalimentación de una manera objetiva y no enjuiciadora, proveyendo información que permita al estudiante comprender sus errores y cómo hacerlo de manera correcta. 
Una retroalimentación informada se centra en las habilidades y competencias actuales del estudiante en comparación con las competencias y habilidades deseadas. Se realiza con la finalidad de desarrollar un plan de acción en conjunto para alcanzar una meta deseada mediante una estrategia de resolución de problemas. No se trata de sólo mirar lo positivo y no enfrentar los conflictos o no conversar sobre aquellos comportamientos distintos a lo esperado, sino de enfocarse en los recursos y fortalezas del estudiante, y no en sus debilidades.  Con esto se anima al estudiante a autoobservarse, autoevaluarse y autorreflexionar sobre sus progresos. 
Sugerencias metodológicas
Ofrecer comentarios del trabajo y progreso realizado de manera lo más descriptiva posible, mostrando al estudiante en primer lugar los aciertos (aquello logrado) para luego señalar los errores o aspectos por lograr, refiriéndose de manera clara y precisa lo que se debe corregir. Esta retroalimentación debe ser individual, de preferencia escrita. Se puede usar rúbrica para mostrar las habilidades logradas.







