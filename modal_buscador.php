<div class="panel">
  <p1>Importancia del involucramiento parental
Hay muchas maneras en que los padres se pueden involucrar en los estudios desde la casa: leyendo juntos, monitoreando las tareas, proveyendo estructura y transmitiendo el valor de la educación, discutiendo aspiraciones futuras y preparándolo para el futuro.  Se ha estudiado que los padres valoran los espacios que los ayudan a desarrollar ciertas competencias para la crianza. En lo que respecta al compromiso escolar se sugiere crear espacios desde la escuela para abordar los siguientes temas:
Cómo pueden ayudar a sus hijos e hijas con las tareas o entregar una estructura de aprendizaje en su rutina con horarios predecibles. Más conversaciones sobre la escuela están asociadas con un mayor compromiso escolar. 
Trabajo en torno a las expectativas: Las expectativas del hogar se refieren a las aspiraciones actuales o futuras en relación con el rendimiento académico, el comportamiento y el logro educativo. Numerosos estudios apoyan la conclusión de que cuando las familias comunican expectativas altas y realistas sobre el trabajo escolar y enfatizan el valor del esfuerzo y el trabajo duro, las y los estudiantes se desempeñan mejor en la escuela; por el contrario, las atribuciones y expectativas inapropiadas pueden desalentar el logro y dificultar su motivación para aprender y desempeñarse bien en la escuela. 

Otras recomendaciones:


Muchos estudios demuestran que prácticas disciplinarias claras, firmes y consistentes, equilibradas con altos niveles de calidez y afecto se asocian con mejores resultados académicos y menores conductas de riesgo. Universalmente, los niños, niñas y adolescentes con niveles más altos de conexión y relaciones positivas y de apoyo con sus padres demuestran un mejor logro, ajuste y comportamiento. 
Las y los estudiantes que reciben más apoyo motivacional de sus padres, tienen un mejor desempeño en la escuela; éste puede ser a través de aliento, mensajes sobre el esfuerzo y la importancia de la educación, regulación del tiempo para el trabajo escolar, y una sana discusión sobre la conexión entre el esfuerzo, la escuela y las metas futuras. 
Fomentar habilidades de interacción y de juego efectivo, así como el establecimiento de límites y estrategias de disciplina respetuosas para manejar problemas de comportamiento.
Mostrar a los padres la importancia de enseñar con el ejemplo las habilidades comunicacionales, el  manejo de la ira, la resolución de problemas, caminos para dar y recibir apoyo, entre otros.
