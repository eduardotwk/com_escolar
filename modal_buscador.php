<button class="accordion">2.-Estrategias de aprendizaje colaborativo entre pares</button>
<div class="panel">
  <p1>Definición
El aprendizaje colaborativo se refiere a la actividad realizada entre pares y en pequeños grupos  dentro o fuera del aula. Guiados por el profesor o profesora, las y los estudiantes forman pequeños equipos después de haber recibido algunas instrucciones.  Dentro de cada equipo intercambian ideas y trabajan en una tarea específica hasta que todos los miembros del equipo la hayan entendido y terminado, aprendiendo así a través de la colaboración. Se favorece que los(as) estudiantes se expliquen mutuamente lo que aprenden. <br />
Se ha demostrado que estos métodos son efectivos para potenciar las habilidades académicas y sociales de los y las estudiantes, siempre y cuando las técnicas de aprendizaje entre pares sean monitoreadas y estructuradas cuidadosamente, poniendo el énfasis no en el logro académico, sino en cómo los niños y niñas interactúan y se relacionan entre ellos. Estas habilidades sociales que desarrollan son tan importantes como las habilidades académicas en la etapa escolar, además, se sabe que, por temor o vergüenza, algunos estudiantes prefieren no responder a las preguntas del o la profesora en clases. Estas estrategias permiten prevenir lo anterior en la medida que se ha comprobado que los y las estudiantes tienen la oportunidad de hablar más libremente y con menos presión cuando están en grupos y en discusiones dirigidas por otros estudiantes. Además, fomentan una mayor comunicación entre los y las estudiantes y conducen a un mayor éxito académico. <br />  
Ejemplos <br />
Tutorías entre pares:  Si un estudiante es bueno y entusiasta en una asignatura, podría enseñarles a sus compañeros y compañeras, en este caso los profesores brindan orientación y están allí para responder preguntas y ayudar a impulsar la discusión. 
Grupos de discusión en clases: se ha comprobado que los y las estudiantes tienen la oportunidad de hablar más libremente y con menos presión cuando están en grupos y en discusiones dirigidas por otros estudiantes.  
Proyectos grupales: Aquí las y los estudiantes trabajan en conjunto analizando diferentes puntos de vista. Como no existe una figura de autoridad a la que acudir para obtener respuestas rápidas, se favorece una discusión más abierta e invita a más personas a unirse con sus opiniones. Esto les empuja a conectar ideas y hacer más investigaciones, haciendo juicios no basados en lo que dice el o la profesor(a), sino en el consenso del grupo a partir del contenido y del material estudiado; así, los conflictos se pueden resolver a través de múltiples enfoques en lugar de acudir al docente para la última palabra.
Foros en líneas: en este contexto las preguntas y respuestas pueden verse desde múltiples puntos de vista y pueden introducirse nuevas ideas. En lugar de que la o el profesor enseñe las respuestas a los problemas, los y las estudiantes pueden explorar las diversas formas de encontrar una respuesta. Se puede integrar actividad de cuestionarios en línea donde estudiantes responden al tiempo que ven otras respuestas. <br />
Fuente http://proyectoaprender.es <br />


Más ejemplos<br />

Clases dirigidas por las y los estudiantes. Ej.: Realizar una presentación sobre un tema, mostrar alguna habilidad específica en algún área.
Aligerar el ambiente: El o la profesora agrupa a las y los estudiantes en equipos heterogéneos, luego presenta un tema y solicita a los equipos que diseñen propuestas absurdas relacionadas con esos contenidos. Por ejemplo: “crea una dieta lo menos nutritiva posible”, “escribe una oración con la mayor cantidad de errores gramaticales”, “diseña un puente destinado a caerse”, etc. Posteriormente elige al azar a uno de los miembros de cada grupo para explicar su creación.
Control grupal: En los días previos a una prueba individual, la o el profesor, agrupa al alumnado en equipos heterogéneos y les entrega un “control grupal” o una prueba similar a la que se realizará de forma individual, los equipos realizan el control respetando la siguiente consigna: no pasarán al siguiente ejercicio hasta que todos los miembros del grupo hayan comprendido el anterior. Al finalizar, el docente realiza una corrección al gran grupo, pidiendo a algunos estudiantes al azar que desarrollen cada ejercicio. Luego los equipos corrigen su control grupal y se evalúan siguiendo las premisas del docente, así cada alumno establece lo que debe repasar para la prueba individual.
Controversia académica: El docente agrupa al alumnado en equipos heterogéneos de cuatro miembros y plantea una afirmación que admite dos posturas: a favor y en contra, luego distribuye dos papeles: una pareja debe defender la afirmación y la otra debe criticarla; se da un tiempo para que las parejas preparen su postura sobre la afirmación, posteriormente los grupos debaten sobre un tema, defendiendo su posición con los argumentos que han trabajado. Para finalizar, los equipos redactan un documento en el que se recogen los argumentos a favor y en contra del tema que defendieron.  <br/>
Fuente http://proyectoaprender.es





