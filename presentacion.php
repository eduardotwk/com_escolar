        <p class="p1">Promover trayectorias educativas positivas ha sido un gran desafío para las escuelas. Para apoyar dicho desafío, un equipo de investigación liderado por la <strong>Dra. Mahia Saracostti</strong> ha diseñado el <strong>Sistema Integrado de Evaluación, Seguimiento y Estrategias de Promoción de Compromiso Escolar y Factores Contextuales (SIESE)</strong> inspirado en la evidencia sobre formas de promover el Compromiso Escolar en el estudiantado. Específicamente, se consideró la evidencia que este mismo equipo ha desarrollado en medición del Compromiso Escolar y los factores contextuales asociados</p>
        <p class="p2">al mismo, así como el modelo del National High School Center at American Institutes for Research (1) implementado en EE.UU para prevenir la desescolarización.
            El SIESE está compuesto de seis pasos que pueden ser implementados tanto en modalidad de educación presencial como a distancia o virtual, el que les invitamos a poner en práctica desde el inicio del año escolar, <strong>con estudiantes de quinto básico a cuarto medio</strong> e incorporarlo como parte de los procedimientos y estrategias de acompañamiento para sus estudiantes. Para lo anterior hemos puesto a disposición de las escuelas esta plataforma para acceder a todos los recursos y sugerencias para la implementación del <strong>SIESE</strong>. Si bien hay ciertos procesos estandarizados, hay espacio para integrar la singularidad de cada comunidad educativa.
            Esperamos que el <strong>SIESE</strong> sea una gran herramienta para apoyar a las escuelas a identificar las necesidades por cada curso y las mejores estrategias para promover el Compromiso Escolar.</p>
